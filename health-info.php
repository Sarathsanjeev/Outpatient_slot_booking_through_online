          <div class="card-body">
              <h2 class="card-title text-center">Stroke</h2>
              <hr>
              <h3>Overview</h3>
              <p class="card-text">A stroke occurs when a blood vessel in the brain ruptures and bleeds, or when there’s a blockage in the blood supply to the brain. The rupture or blockage prevents blood and oxygen from reaching the brain’s tissues.
              </p>
              <p>According to the Centers for Disease Control and Prevention (CDC)Trusted Source, stroke is a leading cause of death in the United States. Every year, more than 795,000 U.S. people have a stroke.</p>
              <p>Without oxygen, brain cells and tissue become damaged and begin to die within minutes.</p>
              <hr>
              <h3>Symptoms</h3>
              <p>The loss of blood flow to the brain damages tissues within the brain. Symptoms of a stroke show up in the body parts controlled by the damaged areas of the brain.</p> 
              <p>The sooner a person having a stroke gets care, the better their outcome is likely to be. For this reason, it’s helpful to know the signs of a stroke so you can act quickly. Stroke symptoms can include:</p>
              <ul>
                  <li>paralysis</li>
                  <li>numbness or weakness in the arm, face, and leg, especially on one side of the body</li>
                  <li>trouble speaking or understanding others</li>
                  </li>slurred speech</li>
                  <li>confusion, disorientation, or lack of responsiveness</li>
                  <li>sudden behavioral changes, especially increased agitation</li>
              </ul>
              
