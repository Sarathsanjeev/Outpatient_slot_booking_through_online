              <hr>
              <h3>Overview</h3>
              <p>Tuberculosis (TB) is a serious illness that mainly affects the lungs. The germs that cause tuberculosis are a type of bacteria.</p>
              <p>Tuberculosis can spread when a person with the illness coughs, sneezes or sings. This can put tiny droplets with the germs into the air. Another person can then breathe in the droplets, and the germs enter the lungs</p>
              <p>Tuberculosis spreads easily where people gather in crowds or where people live in crowded conditions. People with HIV/AIDS and other people with weakened immune systems have a higher risk of catching tuberculosis than people with typical immune systems</p>
              <p>Drugs called antibiotics can treat tuberculosis. But some forms of the bacteria no longer respond well to treatments.</p>
              <hr>
              <h3>Symptoms</h3>
              <ul>
                  <li>Low fever.</li>
                  <li>Tiredness.</li>
                  <li>Cough.</li>
