              <h2 class="card-title text-center">Diabetes</h2>
              <hr>
              <h3>Overview</h3>
              <p class="card-text">Diabetes is a chronic disease that occurs either when the pancreas does not produce enough insulin or when the body cannot effectively use the insulin it produces. Insulin is a hormone that regulates blood glucose. Hyperglycaemia, also called raised blood glucose or raised blood sugar, is a common effect of uncontrolled diabetes and over time leads to serious damage to many of the body's systems, especially the nerves and blood vessels.</p>
              <p>In 2014, 8.5% of adults aged 18 years and older had diabetes. In 2019, diabetes was the direct cause of 1.5 million deaths and 48% of all deaths due to diabetes occurred before the age of 70 years. Another 460 000 kidney disease deaths were caused by diabetes, and raised blood glucose causes around 20% of cardiovascular deaths</p>
              <p>Between 2000 and 2019, there was a 3% increase in age-standardized mortality rates from diabetes. In lower-middle-income countries, the mortality rate due to diabetes increased</p>
              <p>By contrast, the probability of dying from any one of the four main noncommunicable diseases (cardiovascular diseases, cancer, chronic respiratory diseases or diabetes) between the ages of 30 and 70 decreased by 22% globally between 2000 and 2019</p>
              <hr>
              <h3>Symptoms</h3>
              <ul>
                  <li>Urinate (pee) a lot, often at night.</li>
                  <li>Are very thirsty.</li>
                  <li>Lose weight without trying.</li>
                  </li>Are very hungry.</li>
                  <li>Have blurry vision.</li>
                  <li>Have numb or tingling hands or feet.</li>
                  <li>Feel very tired.</li>
                  <li>Have very dry skin.</li>
              </ul>   
